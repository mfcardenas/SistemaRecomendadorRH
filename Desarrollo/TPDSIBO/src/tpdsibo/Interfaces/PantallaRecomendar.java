    public void poblarComboPuestos(){
        try{
            List<Puesto> puestos = StardogControl.getInstancia().obtenerPuestosParaCombo();
            
            DefaultComboBoxModel comboModel = new DefaultComboBoxModel(puestos.toArray());
            cmbPuestos.setModel(comboModel);
            

        }
        catch(Exception e){//Error
            System.out.print(e);
        }
    }
    

    public void poblarTablaPostulantes(List<Persona> postulantes){
        String[] columnas = {"URI", "Edad", "Sexo", "DesempeÃ±o","Liderazgo","Habilitado"};
        TablaPostulantesTableModel tblModel = new TablaPostulantesTableModel(postulantes, columnas);
        
 
        tblPostulantes.setModel(tblModel); //Se agrega el tblModel a la tabla
        
        
        
        tblPostulantes.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
        public void valueChanged(ListSelectionEvent event) {
            TablaPostulantesTableModel m = (TablaPostulantesTableModel) tblPostulantes.getModel();
            Persona postulanteSeleccionado = m.getPostulantePorFila(tblPostulantes.getSelectedRow());
            poblarAntecedentes(postulanteSeleccionado);
        }
    });
            
            
        }
    
    public void poblarAntecedentes(Persona p){
        List<String> antecedentes = new ArrayList<String>();
        if(p != null){
            

            for (Puesto puesto : p.getAntecedentes()){
                antecedentes.add(puesto.getNombre());
            }
        }
        
        actualizarAntecedentes(antecedentes);
        
    }
    
    private void actualizarAntecedentes(List<String> l) {
        DefaultListModel listModel = new DefaultListModel();
        
        for (String s : l)
            listModel.addElement(s);
        
        jList1.setModel(listModel);
        
    }
    

